#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	unsigned int	i;
	unsigned int	j;
	unsigned int	i_l;

	i = 0;
	while (dst[i])
		i++;
	if (i > size)
		i_l = size;
	else
		i_l = i;
	j = 0;
	while (src[j] && i < size - 1)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	while (src[j])
		j++;
	return (i_l + j);
}
